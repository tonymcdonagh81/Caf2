	/**
	 * query that returns all the shops
	 * @return
	 */
 public List <Shop> getAllShops() {
  EntityManager em = EMFService.get().createEntityManager();
  // read the existing entries
  Query q = em.createQuery("select m from Shop m");
  List <Shop> shops = q.getResultList();
  return shops;
 }


	/**
	 * query that returns shops based on the users location and radius
	 * @return
	 */
 public List < Shop > getNearby(double lat, double lng, double radius) {
	 EntityManager em = EMFService.get().createEntityManager();
	 CalNear calNear = new CalNear();
	 calNear.calLatLng(lat,lng, radius);
	 double latMax = calNear.latMax;
	 double latMin = calNear.latMin;
	 double lngMax = calNear.lngMax;
	 double lngMin = calNear.lngMin;

	 Query q = em.createQuery("SELECT t FROM Shop t where t.lat >= :latMin and t.lat <= :latMax");
	 Query p = em.createQuery("SELECT s FROM Shop s where s.lng >= :lngMin and s.lng <= :lngMax");
	 q.setParameter("latMin", latMin);
	 q.setParameter("latMax", latMax);
	 p.setParameter("lngMin", lngMin);
	 p.setParameter("lngMax", lngMax);
	 List < Shop > latList = q.getResultList();
	 List < Shop > lngList = p.getResultList();
	 List < Shop > shops = calNear.compareLists(latList, lngList);
	 return shops;
 }
 
 

